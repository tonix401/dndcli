import { primaryColor, secondaryColor } from "@utilities/ConsoleService.js";

export function getShopVisual(books: boolean = false, swords: boolean = false, bottles: boolean = false) {
  // books
  const bs = books ? primaryColor : (text: string) => text;
  // swords
  const s = swords ? primaryColor : (text: string) => text;
  // bottles
  const b = bottles ? primaryColor : (text: string) => text;
  
  const lines = [
    `*******************************************************************************`,
    `              /                                                / /             `,
    `             /________________________________________________/ /              `,
    `            |                                                 |/               `,
    `            (_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)(_)                `,
    `                ||  ||  ${bs("______")}                           ||  ||                `,
    `                ||  || ${bs("/     /|")}                      ${b("_")}   ||  ||                `,
    `                ||  ||${bs("/____ //")}                     ${b("_/ \\")}  ||  ||                `,
    `                ||  |${bs("(_____|//|")}___________________${b("/ \\+ )")}_||__||                `,
    `                || /${bs("(_____| //")}   ${s("o={==========-")}  ${b("( + )_|")} ||  /|                `,
    `                ||/  ${bs("(_____|/")}   ${s("o={==========-")}   ${b("|___|")}   || / |                `,
    `_______________ ||_______________________________________||/  | _______________`,
    `/______/______/ |‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾|   | ______/______/_`,
    `____/______/___ |             (‾ |_| /‾\\ |‾)              |  /|____/______/____`,
    `/______/______/ |             _) | | \\_/ |‾               | / _/______/______/_`,
    `____/______/___ |_________________________________________|/ ______/______/____`,
    `/______/______/ ||‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾‾|| ___/______/______/_`,
    `____/______/______/______/______/______/______/______/______/______/______/____`,
  ];
  
  return secondaryColor(lines.join("\n"));
}